#include <bits/stdc++.h>
using namespace std;
int main ()
{
    //khai báo hàng, cột và nhập vào hàng, cột:
    int n, m ; cin >> n >> m;
    //khai bảo mảng hai chiều:
    int mang[n][m];
    //nhập vào mảng 
    for(int i = 0; i < n; i++)
    {
        for(int j = 0; j < m; j++)
        {
            cin >> mang[i][j];
        }
    }

    //thuật toán; tìm phần tử nhỏ nhất trong hàng và xem phần tử đó có lớn nhất trong cột không 
    
}